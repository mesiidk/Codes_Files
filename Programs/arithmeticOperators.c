#include <stdio.h>
#include <math.h>

int main() {

    // arithmetic operators

    // + = (addition)
    // - = (subtraction)
    // * = (multiplication)
    // % = (modulus)
    // ++ = increment
    // -- = decrement

    double num1 = 5;
    double num2 = 5;

    printf("num1 + num2 = %lf, This is addition\n", num1 + num2);

    double num3 = 7.5;
    double num4 = 7.5;

    printf("num3 - num4 = %lf, This is subtraction\n", num3 - num4);

    double num5 = 7.5;
    double num6 = 7.5;

    printf("num5 * num6 = %lf, This is multiplication\n", num5 * num6);

    double num7 = 10;

    printf("num7 %% 2 = %d, This is modulus\n", 10%2);

    double num8 = 1;

    printf("num8++ = %lf, This is increment\n", num8 = num8 + 1);

    double num9 = 1;

    printf("num9-- = %lf, This is decrement\n", num9 = num9 - 1);




    return 0;
}
